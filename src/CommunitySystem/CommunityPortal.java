    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Community co = new Community();
        City c = new City();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        //final Object[] row= new Object[2];
        if(name.getText().equals("") )
        {
            JOptionPane.showMessageDialog(null,"Please fill complete information");
        }else{
//            row[0]=name.getText();
//            row[1]=city.getSelectedItem().toString();
              c.setCityName(cityN.getSelectedItem().toString());
              city.getCity().add(c);
              co.setCommunityName(name.getText());
              co.setCity(c);
//              c.setCity(cityN);
              community.getCommunity().add(co);
            
            //model.addRow(row);
              for(Community e: community.getCommunity()){

                Object[] row1={e.getCommunityName(),e.getCity().getCityName() };
                model.addRow(row1);

            }
            name.setText("");
            JOptionPane.showMessageDialog(null,"Saved Successfully");

        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        int i = jTable1.getSelectedRow();
        if(i>=0){
            model.removeRow(i);
            community.getCommunity().remove(i);
            name.setText("");
            JOptionPane.showMessageDialog(null,"Deleted Successfully");
        }else{
            JOptionPane.showMessageDialog(null,"Please select a row first");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

